func NormalizeURL(str string) (string, error) {
	if !IsURL(str) {
		return "", errors.New("INVALID_URI")
	}

	if !(strings.HasPrefix(str, "https://") || strings.HasPrefix(str, "http://")) {
		str = "https://" + str
	}
	str, err := purell.NormalizeURLString(str, purell.FlagsSafe)

	return str, err
}
